void HAL_GPIO_EXTI_Callback(uint16_t GPIO_Pin)
{
    // 我们只处理PA0的中断，在PA0的上升沿检测PA1的电平
    if (GPIO_Pin == GPIO_PIN_0)
    {
        // 如果CLK(PA0)为高电平时，DT(PA1)为低电平，则为顺时针
        if (HAL_GPIO_ReadPin(GPIOA, GPIO_PIN_1) == GPIO_PIN_RESET)
        {
            counter++;
        }
        else // 否则为逆时针
        {
            counter--;
        }
    }
}
